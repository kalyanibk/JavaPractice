package loopArray;

public class NameLoops {

	public static void main(String[] args) {
		char[] name= new char[8];
		
		name[0]='K';
		name[1]='A';
		name[2]='L';
		name[3]='Y';
		name[4]='A';
		name[5]='N';
		name[6]='I';
		name[7]='E';
		
		for(int i=0;i<=7;i++) {
			System.out.println("Name-> "+ name[i]);
		}
		

	}

}
